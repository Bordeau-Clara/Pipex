#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t siz)
{
	size_t	i;
	size_t	dest_len;
	size_t	src_len;

	i = 0;
	dest_len = 0;
	src_len = 0;
	while (dst[dest_len])
		dest_len++;
	while (src[src_len])
		src_len++;
	if (siz <= dest_len)
		return (src_len + siz);
	while ((i + dest_len) < (siz - 1) && src[i])
	{
		dst[dest_len + i] = src[i];
		i++;
	}
	dst[dest_len + i] = '\0';
	return (src_len + dest_len);
}
